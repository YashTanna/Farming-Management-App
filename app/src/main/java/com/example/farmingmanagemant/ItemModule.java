package com.example.farmingmanagemant;

public class ItemModule {
    String number;
    String name;
    String type;
    String price;
    String location;
    String time;

    public ItemModule(String number, String name, String type, String price, String location, String time) {
        this.number = number;
        this.name = name;
        this.type = type;
        this.price = price;
        this.location = location;
        this.time = time;
    }
}
